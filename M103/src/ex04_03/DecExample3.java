package ex04_03;

public class DecExample3 {
	public static void main(String args[]) {
		short num1 = 12; // num1 변수의 선언문
		System.out.println(num1);
		double num2 = 12.75; // num2 변수의 선언문
		System.out.println(num2);
		char ch = 'A'; // ch 변수 선언문
		System.out.println(ch);
		System.out.println(num1 + ", " + num2 + ", " + ch);

		short num3 = 11; // num3 변수의 선언문
		System.out.print(num3 + ", ");
		double num4 = 11.75; // num4 변수의 선언문
		System.out.print(num4 + ", ");
		char ch1 = 'B'; // ch 변수 선언문
		System.out.print(ch1);

	}

}

// 즉, 사용하기 전까지만 변수를 선언만 하기만 하면 실행이 가능하다.

