package ex04_10;

public class Oper10 {
	public static void main(String args[]){
		int n1=10, n2=10, n3=10, n4 =10, n5 = 10;
		
		System.out.println(++n1);
		System.out.println(n1);
		
		// System.out.println(++n5 + n5); = 22
		
		System.out.println(n2++);
		System.out.println(n2);
		
		System.out.println(--n3);
		System.out.println(n3);
		
		System.out.println(n4--);
		System.out.println(n4);
	}

}
