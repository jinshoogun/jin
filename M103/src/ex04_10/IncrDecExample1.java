package ex04_10;

public class IncrDecExample1 {
	public static void main(String args[]) {
		int num = 0;
		System.out.println(num);
		num++;
		System.out.println(num);
		++num;
		System.out.println(num);
		num--;
		System.out.println(num);
		--num;
		System.out.println(num);

	}

}
