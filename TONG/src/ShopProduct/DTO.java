package ShopProduct;

public class DTO {
	
	private int s_no;
	private String s_name;
	private String s_price;
	private String s_derail;
	private String s_stock;
	private String s_image;
	private String s_date;
	public int getS_no() {
		return s_no;
	}
	public void setS_no(int s_no) {
		this.s_no = s_no;
	}
	public String getS_name() {
		return s_name;
	}
	public void setS_name(String s_name) {
		this.s_name = s_name;
	}
	public String getS_price() {
		return s_price;
	}
	public void setS_price(String s_price) {
		this.s_price = s_price;
	}
	public String getS_derail() {
		return s_derail;
	}
	public void setS_derail(String s_derail) {
		this.s_derail = s_derail;
	}
	public String getS_stock() {
		return s_stock;
	}
	public void setS_stock(String s_stock) {
		this.s_stock = s_stock;
	}
	public String getS_image() {
		return s_image;
	}
	public void setS_image(String s_image) {
		this.s_image = s_image;
	}
	public String getS_date() {
		return s_date;
	}
	public void setS_date(String s_date) {
		this.s_date = s_date;
	}
	


}

